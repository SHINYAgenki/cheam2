// 逆ShiftRows関数
void invShiftRows(unsigned char *dst, unsigned char *src) {
    unsigned char tmp;
    dst[0] = src[0];
    dst[4] = src[4];
    dst[8] = src[8];
    dst[12] = src[12];
    tmp = src[13];
    dst[13] = src[9];
    dst[9] = src[5];
    dst[5] = src[1];
    dst[1] = tmp;
    tmp = src[2];
    dst[2] = src[10];
    dst[10] = tmp;
    tmp = src[6];
    dst[6] = src[14];
    dst[14] = tmp;
    tmp = src[3];
    dst[3] = src[7];
    dst[7] = src[11];
    dst[11] = src[15];
    dst[15] = tmp;
}

// 逆SubBytes関数
void invSubBytes(unsigned char *dst, unsigned char *src) {
    for (int i = 0; i < 16; i++) {
        dst[i] = g_inv_sbox[src[i]];
    }
}

// 平文ペアからラウンドキーを求める
void deriveKey(unsigned char *key, unsigned char *pt, unsigned char *pt_prime) {
    unsigned char m_prime[16];
    unsigned char m_prime_prime[16];
    
    // invShiftRowsとinvSubBytesで1ラウンド目の出力状態を逆算する
    invShiftRows(m_prime, pt);
    invSubBytes(m_prime_prime, pt_prime);

    // 平文mとm_primeをXORして鍵kを得る
    for (int i = 0; i < 16; i++) {
        key[i] = m_prime[i] ^ m_prime_prime[i];
    }
}

